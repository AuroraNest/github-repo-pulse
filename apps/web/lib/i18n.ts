export const supportedLocales = ["en", "zh"] as const;

export type Locale = (typeof supportedLocales)[number];

export const localeCookieName = "repopulse_locale";

export function isSupportedLocale(value: string | undefined): value is Locale {
  return supportedLocales.includes(value as Locale);
}

export function toIntlLocale(locale: Locale) {
  return locale === "zh" ? "zh-CN" : "en";
}

export function translateVisibility(value: string, locale: Locale) {
  const labels: Record<Locale, Record<string, string>> = {
    en: { public: "Public", private: "Private" },
    zh: { public: "公开", private: "私有" }
  };

  return labels[locale][value] ?? value;
}

export function translateStatus(value: string, locale: Locale) {
  const labels: Record<Locale, Record<string, string>> = {
    en: {
      healthy: "Healthy",
      warning: "Warning",
      success: "Success",
      partial_failed: "Partial failed",
      Active: "Active",
      "Baseline captured": "Baseline captured",
      Warning: "Warning"
    },
    zh: {
      healthy: "健康",
      warning: "告警",
      success: "成功",
      partial_failed: "部分失败",
      Active: "启用",
      "Baseline captured": "基线已记录",
      Warning: "告警"
    }
  };

  return labels[locale][value] ?? value;
}

